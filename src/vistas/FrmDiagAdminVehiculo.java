    private void configurarTabla() {
        String cabecera[] = {"ID", "Marca ", "Color", "Placa", "Cliente"};
        String datos[][] = {};
        this.modeloTabla = new DefaultTableModel(datos, cabecera) {
            @Override
            public boolean isCellEditable(int fila, int columna) {
                return false;
            }
        };
        this.tblListaDeVehiculo.setModel(this.modeloTabla);
    }

    private void refrescarTabla() {
        //Elimina los registros del modelo y de la tabla
        this.modeloTabla.setNumRows(0);

        //Agregar información de la base de datos a la tabla
        String filaDeTabla[] = new String[5];

        for (Object obj : objAdminVehiculoDB.listarTodo2()) {
            VehiculoVista objVehiculo = (VehiculoVista) obj;
            filaDeTabla[0] = Integer.toString(objVehiculo.getIdv());
            filaDeTabla[1] = objVehiculo.getMarca_v();
            filaDeTabla[2] = objVehiculo.getColor_v();
            filaDeTabla[3] = objVehiculo.getPlaca_v();
            filaDeTabla[4] = objVehiculo.getCliente();

            this.modeloTabla.addRow(filaDeTabla);
        }
    }

