    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = this.tblListaDeVehiculo.getSelectedRow();

        if (fila > -1) {
            int id = Integer.parseInt(this.tblListaDeVehiculo.getValueAt(fila, 0).toString());

            FrmDiagActualizarVehiculo objFrmDiagUpdArts = new FrmDiagActualizarVehiculo(null, true, id);
            objFrmDiagUpdArts.setLocationRelativeTo(null);
            objFrmDiagUpdArts.setVisible(true);

            this.refrescarTabla();

        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione el vehiculo que quiere actualizar.");
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = this.tblListaDeVehiculo.getSelectedRow();

        if (fila > -1) {
            int idv = Integer.parseInt(this.tblListaDeVehiculo.getValueAt(fila, 0).toString());

            Vehiculo objVehiculo = new Vehiculo();
            objVehiculo.setIdv(idv);

            if (objAdminVehiculoDB.eliminar(objVehiculo)) {
                JOptionPane.showMessageDialog(this, "El vehiculo fue eliminado con éxito.");
            } else {
                JOptionPane.showMessageDialog(this, "El vehiculo no pudo ser eliminado.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione el vehiculo que quiere eliminar.");
        }

        this.refrescarTabla();
    }//GEN-LAST:event_btnEliminarActionPerformed

